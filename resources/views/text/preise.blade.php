<h3>Kostenrückerstattung</h3>

<article class="flex-center"> Für eine Rückerstattung der Therapiekosten bei Ihrer Krankenkasse benötigen Sie eine
    Verordnung für Physiotherapie
    von Ihrem Haus- oder Facharzt.
</article>
<article class="flex-center"> Diese muss chefärztlich von Ihrer Krankenkasse bewilligt werden (übernehmen wir auch
    gerne für Sie).
</article>


<article class="flex-center">Nach Therapieabschluss erhalten Sie eine Rechnung, die Sie gemeinsam mit der
    bewilligten
    Verordnung bei Ihrer
    Krankenkasse einreichen.
</article>

<article class="flex-center">Sie bekommen einen Teil Ihrer Kosten (unterschiedlicher Teil je nach Kasse) von Ihrer
    Krankenkasse rückerstattet.
    Verbleibende Restkosten können von Ihrer Zusatzversicherung übernommen werden.
</article>

